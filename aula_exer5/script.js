// Pedir para o usuário digitar dois números
// e mostrar um boolean indicando se
// a média for maior que 5

let n1 = parseFloat(prompt(`Digite um número:`));
let n2 = parseFloat(prompt(`Digite outro número:`));

let media = (n1 + n2) / 2;
alert(media > 5);


